# Класс - шаблон объекта
class Car:
    wheels = 4
    engine = 1
    color = "white"












# Объект - экземпляр класса

mersedes = Car()
audi = Car()
ferrari = Car()

print(mersedes)
print(audi)
print(ferrari)

print(mersedes.color)
print(audi.color)
print(ferrari.color)

mersedes.color = 'grey'
audi.color = 'pink'
ferrari.color = 'red'

print(mersedes.color)
print(audi.color)
print(ferrari.color)



