for num in range(185311, 185331):
    deliteli = []
    for d in range(1, int(num**0.5) + 1):
        if num % d == 0:
            deliteli.append(d)
            if num//d != d:
                deliteli.append(num//d)
    if len(deliteli) == 4:
        deliteli.sort()
        print(deliteli[0],deliteli[1],deliteli[2],deliteli[3])
        
        
        
        
        
        
    
    
    
 
